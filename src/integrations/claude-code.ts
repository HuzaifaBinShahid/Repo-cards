import * as path from 'path';
import { upsertBlock, UpsertStatus } from './common';

export async function installClaudeCode(
  root: string,
  toolName: string,
): Promise<{ file: string; status: UpsertStatus }> {
  const body = `## Repo context — repocards

This repository has a pre-computed context folder at \`.${toolName}/\`. **Open [\`.${toolName}/AGENT_GUIDE.md\`](./.${toolName}/AGENT_GUIDE.md) at the start of any task about this project** — before running Grep/Glob/Read on the source, and also when the user asks health-check questions like "are you reading repocards?". The guide routes you to pre-computed cards (architecture, entrypoints, api-surface) and contains self-identification instructions. Typical card read is 10–50× fewer tokens than a grep pass.

Generated by [repocards](https://www.npmjs.com/package/repocards). Re-run \`npx ${toolName} index\` after code changes.`;
  const status = await upsertBlock(path.join(root, 'CLAUDE.md'), toolName, body);
  return { file: 'CLAUDE.md', status };
}
