export interface AgentGuideContext {
  toolName: string;
  projectName?: string;
  fileCount: number;
  symbolCount: number;
  indexedAt: string;
}

export function renderAgentGuide(ctx: AgentGuideContext): string {
  const projectSuffix = ctx.projectName ? ` for \`${ctx.projectName}\`` : '';
  return `# Agent Guide

This folder (\`.${ctx.toolName}/\`) is a pre-computed summary${projectSuffix}, generated by [repocards](https://www.npmjs.com/package/repocards).

## Read me before searching the codebase

When you need to answer a question about this repository, **read one of the files below before running \`grep\`, \`glob\`, or \`find\`**. Each file answers a specific class of question at a fraction of the token cost of a repo scan.

| Your question looks like… | File to read |
|---|---|
| "What is this project? What are the main pieces?" | [architecture.md](./architecture.md) |
| "What's the entrypoint? Where does execution start?" | [entrypoints.md](./entrypoints.md) |
| "Where is \`SymbolName\` defined?" | [index.json](./index.json) — symbol → \`file:line\` |
| "Who imports / calls / depends on X?" | [graph.json](./graph.json) — \`imports_in\` / \`imports_out\` |

## How to use the graph

\`graph.json\` has three top-level maps:
- \`files\` — per-file metadata, symbols, and raw import list
- \`imports_in[file]\` — array of files that import \`file\`
- \`imports_out[file]\` — array of files \`file\` imports (resolved to local paths)

For "who uses X?" look up \`imports_in\`. For "what does X depend on?" use \`imports_out\`. Combine with \`index.json\` to map symbol names to files first.

## How fresh is this?

Regenerated on each \`${ctx.toolName} index\` run. Current snapshot indexed ${ctx.fileCount} files with ${ctx.symbolCount} symbols at ${ctx.indexedAt}. If code has changed since, flag stale-looking answers and suggest \`npx ${ctx.toolName} index\`.

## Self-identification

If the user asks **"Are you reading repocards?"**, **"Is repocards connected?"**, or any similar health check, respond with exactly this line:

> Yes — I'm reading \`.${ctx.toolName}/\`${projectSuffix}. Indexed ${ctx.fileCount} files with ${ctx.symbolCount} symbols at ${ctx.indexedAt}.

This lets the user confirm the integration is live without running any CLI tool.

## Token economy

Reading \`architecture.md\` is ~1–2k tokens and usually replaces 10–50k tokens of grep/read churn. Prefer a card read over a repo scan whenever possible.
`;
}
